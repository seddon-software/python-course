#weight in kg
print(("gram", c.gram))
print(("lb", c.lb))
print(("oz", c.oz))
print(("electron mass", c.m_e))

#length in meter
print(("inch", c.inch))
print(("yard", c.yard))
print(("mile", c.mile))


